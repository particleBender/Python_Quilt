import turtle
import math

def patternA(patPosX, patPosY, sq_size, t):
    ##############################################################################################
    #----------------------------------------- SETUP --------------------------------------------#
    ##############################################################################################
    # Change color value format to RGB 0-255
    turtle.colormode(255)
    # Pen draw size
    t.width(2)

    ##############################################################################################
    #------------------------------------- MOVE FUNCTION ----------------------------------------#
    ##############################################################################################
    # To move without drawing
    def move(positionX,positionY,t):
        t.pu()
        t.setpos(patPosX+positionX,patPosY+positionY)
        t.pd()

    ##############################################################################################
    #--------------------------------- DRAW PURPLE SQUARE ---------------------------------------#
    ##############################################################################################
    t.fillcolor(132,56,128)
    move(-sq_size*.4,sq_size*.4,t)
    t.fill(True)
    for i in range(0,4):
        t.fd(sq_size*.8)
        t.right(90)
    t.fill(False)


    ##############################################################################################
    #--------------------------------- DRAW PINK DIAMOND ----------------------------------------#
    ##############################################################################################
    t.fillcolor(192,82,83)
    move(0,-sq_size*.4,t)
    t.fill(True)
    t.circle(sq_size*.4, 360, 4)
    t.fill(False)

    ##############################################################################################
    #--------------------------------- DRAW BLUE DIAMOND ----------------------------------------#
    ##############################################################################################
    t.fillcolor(97,168,186)
    move(0,-sq_size*.2,t)
    t.fill(True)
    t.circle(sq_size*.2, 360, 4)

    ##############################################################################################
    #--------------------------- YELLOW/BROWN SQUARE FUNCTION -----------------------------------#
    ##############################################################################################
    def brownTriangle(t):
        # Makes a yellow square
        t.fillcolor(233,151,67)
        t.fill(True)
        for i in range(0,4):
            t.fd(sq_size*.2)
            t.right(90)
        t.fill(False)
        # Makes a brown triangle on top of the yellow square
        t.fillcolor(137,76,48)
        t.fill(True)
        t.fd(sq_size*.2)
        t.right(135)
        t.fd(math.sqrt((sq_size*.2)**2 + (sq_size*.2)**2))
        t.right(135)
        t.fd(sq_size*.2)
        t.fill(False)

    ##############################################################################################
    #----------------------------- DRAW YELLOW/BROWN SQUARE -------------------------------------#
    ##############################################################################################

    move(-sq_size*.4,sq_size*.4,t)
    brownTriangle(t)
    move(-sq_size*.4,-sq_size*.4,t)
    brownTriangle(t)
    move(sq_size*.4,-sq_size*.4,t)
    brownTriangle(t)
    move(sq_size*.4,sq_size*.4,t)
    brownTriangle(t)


    ##############################################################################################
    #----------------------------- YELLOW TRIANGLE FUNCTION -------------------------------------#
    ##############################################################################################
    def yellowTriangle(t):
        t.fillcolor(233,151,67)
        t.fill(True)
        t.right(45)
        t.fd(math.sqrt((sq_size*.2)**2 + (sq_size*.2)**2))
        t.right(90)
        t.fd(math.sqrt((sq_size*.2)**2 + (sq_size*.2)**2))
        t.right(135)
        t.fd(sq_size*.4)
        t.fill(False)

    ##############################################################################################
    #------------------------------ DRAW YELLOW TRIANGLES ---------------------------------------#
    ##############################################################################################
    move(-sq_size*.4,sq_size*.2,t)
    yellowTriangle(t)
    move(-sq_size*.2,-sq_size*.4,t)
    yellowTriangle(t)
    move(sq_size*.4,-sq_size*.2,t)
    yellowTriangle(t)
    move(sq_size*.2,sq_size*.4,t)
    yellowTriangle(t)
